<template>
  <div class="pie-chart">
    <div class="pie-chart--item">
      <canvas :id="'pieChart_' + item.id" width="100" height="100" v-if="showCanvas"></canvas>
    </div>
    <div class="text-center">{{ item.name }}</div>
  </div>
</template>

<script>
import echarts from 'echarts'

export default {
  name: 'pie-chart',
  props: {
    item: {
      type: Object,
      required: true
    }
  },
  data() {
    return {
      showCanvas: true
    }
  },
  watch: {
    item(val) {
      let precent = val.divide ? ((val.divide / val.total) * 100).toFixed(1) + '%' : '0.0%'
      let PIRadio = val.divide ? (val.divide / val.total).toFixed(3) * 2 - 0.5 : -0.5
      this.showCanvas = false
      this.$nextTick(() => {
        this.showCanvas = true
        this.$nextTick(() => {
          this.chartRadio(precent, PIRadio)
        })
      })
    }
  },
  methods: {
    chartRadio(precent, PIRadio) {
      let pieChart = document.getElementById(`pieChart_${this.item.id}`)
      let ctx = pieChart.getContext('2d')
      ctx.beginPath()
      ctx.strokeStyle = '#96a1b6'
      ctx.arc(50, 50, 35, 0, 2 * Math.PI, false)
      ctx.stroke()
      ctx.beginPath()
      ctx.strokeStyle = '#363f51'
      ctx.lineWidth = 6
      ctx.arc(50, 50, 38, -0.5 * Math.PI, PIRadio * Math.PI, false)
      ctx.stroke()
      ctx.font = '14px sans-serif'
      ctx.fillText(precent, 32, 57)
    },
  }
}
</script>

<style lang="less" scoped>
.pie-chart {
  transform: translateY(-27px);
}
.pie-chart--item {
  width: 100px;
  height: 100px;
  margin: 0 auto;
}
</style>